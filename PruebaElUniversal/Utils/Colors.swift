import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var blueUniversal: UIColor{
        return UIColor(red: 9/255, green: 83/255, blue: 128/255, alpha: 1)
    }
    
    @nonobjc class var grayUniversal: UIColor{
        return UIColor(red: 58/255, green: 58/255, blue: 58/255, alpha: 0.2)
    }
    @nonobjc class var blackUniversal: UIColor{
        return UIColor(red: 0/255, green: 0/255, blue: 0/255, alpha: 1)
    }
}
